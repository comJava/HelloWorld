package com.chinamobile.athena.risk.common.entity;

import com.google.gson.annotations.SerializedName;

public class ResBody {
	private String resultCode="200";
	private String resultString="SUCC";
	@SerializedName("risk_level")
	private String riskLevel;
	@SerializedName("risk_event")
	private String riskEvent;
	@SerializedName("responsetime")
	private String responseTime;

	public String getResultCode() {
		return resultCode;
	}

	public void setResultCode(String resultCode) {
		this.resultCode = resultCode;
	}

	public String getResultString() {
		return resultString;
	}

	public void setResultString(String resultString) {
		this.resultString = resultString;
	}

	public String getRiskLevel() {
		return riskLevel;
	}

	public void setRiskLevel(String riskLevel) {
		this.riskLevel = riskLevel;
	}

	public String getRiskEvent() {
		return riskEvent;
	}

	public void setRiskEvent(String riskEvent) {
		this.riskEvent = riskEvent;
	}

	public String getResponseTime() {
		return responseTime;
	}

	public void setResponseTime(String responseTime) {
		this.responseTime = responseTime;
	}

}
